<p>Onayınıza sunulan bu a&ccedil;ık rıza metni ile aşağıda belirtilen faaliyet ve işleme ama&ccedil;larıyla sınırlı olarak şirketimizin kişisel verilerinizi işlemesine izin vermektesiniz. Bu &ccedil;er&ccedil;evede, &ouml;zg&uuml;r iradenizle verdiğiniz a&ccedil;ık rızanıza dayalı olarak şirketimiz tarafından kişisel verilerinizin işleneceği faaliyetler ve ama&ccedil;lar ve ilgili cookieler aşağıda belirtilmektedir.</p>



