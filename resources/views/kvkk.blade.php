<p>Packsion, kişisel verilerinizi Packsion Anonim Şirketi Kişisel Verilerin Korunması ve İşlenmesi M&uuml;şteri Aydınlatma Metni&rsquo;nde a&ccedil;ıklanan şekilde, 6698 sayılı Kişisel Verilerin Korunması Kanunu (&ldquo;<strong>Kanun</strong>&rdquo;) ve ilgili mevzuat h&uuml;k&uuml;mlerine uygun olarak işlemekte ve korumaktadır.</p>





<p><strong>Cookie (&ldquo;&Ccedil;erez&rdquo;) Nedir?</strong></p>



<p>Cookieler ziyaret edilen web siteleri aracılığıyla bilgisayar, tablet, telefon gibi internet aksamlı cihazlara veya ağ sunucusuna depolanan k&uuml;&ccedil;&uuml;k metin dosyalarıdır. Cookieler, ziyaret ettiğiniz web sitesiyle ilişkili sunucular tarafından oluşturulurlar. B&ouml;ylelikle ziyaret&ccedil;i aynı siteyi ziyaret ettiğinde sunucu bunu anlayabilir.</p>



<p><strong>Cookielerin Kullanılma Amacı Nedir? </strong></p>



<p>Cookieler, internet sitesinin işleyişinin iyileştirilmesi ve daha efektif &ccedil;alışmasının sağlanması, ziyaret&ccedil;ilerin internet sitesi &uuml;zerindeki hareketlerini takip ederek kullanıcı deneyiminin artırılması, istatistik oluşturulması, profilleme yapılması ve ziyaret&ccedil;ilere &ouml;zel &ouml;neriler oluşturulması ama&ccedil;larıyla kullanılmaktadır</p>



<p><strong>İnternet Sitemizde Kullanılan Cookielerin S&uuml;releri Nedir?</strong></p>



<table class="table table-bordered">
	<tbody>
		<tr>
			<td>
			<p>Oturum Cookieleri</p>

			<p><em>(Session Cookies)</em></p>
			</td>
			<td>
			<p>Oturum cookieleri ziyaret&ccedil;ilerimizin İnternet Sitesi&rsquo;ne ziyaret s&uuml;resince kullanılan, tarayıcı kapatıldıktan sonra silinen ge&ccedil;ici cookielerdir.</p>

			

			<p><a name="_GoBack"></a>Bu t&uuml;r cookielerin kullanılmasının temel amacı ziyaretiniz İnternet Sitesi&rsquo;nin d&uuml;zg&uuml;n bir bi&ccedil;imde &ccedil;alışmasının teminini sağlamaktır.</p>

			<p>&Ouml;rneğin: birden fazla sayfadan oluşan &ccedil;evrimi&ccedil;i formları doldurmanızın sağlanmaktadır.</p>
			</td>
		</tr>
		<tr>
			<td>
			<p>Kalıcı Cookieler</p>

			<p><em>(Persistent Cookies)</em></p>
			</td>
			<td>
			<p>Kalıcı cookieler İnternet Sitesi&rsquo;nin işlevselliğini artırmak, ziyaret&ccedil;ilerimize daha hızlı ve iyi bir hizmet sunmak amacıyla kullanılan cookie t&uuml;rleridir.</p>

			

			<p>Bu t&uuml;r cookieler tercihlerinizi hatırlamak i&ccedil;in kullanılır ve tarayıcılar vasıtasıyla cihazınızda depolanır.</p>

			

			<p>Kalıcı cookielerin bazı t&uuml;rleri, İnternet Sitesi&rsquo;ni kullanım amacınız gibi hususlar g&ouml;z &ouml;n&uuml;nde bulundurarak sizlere &ouml;zel &ouml;neriler sunulması i&ccedil;in kullanılabilmektedir.</p>

			

			<p>Kalıcı cookieler sayesinde İnternet Sitesi&rsquo;ni aynı cihazla tekrardan ziyaret etmeniz durumunda, cihazınızda İnternet Sitesi tarafından oluşturulmuş bir cookie olup olmadığı kontrol edilir ve var ise, sizin İnternet Sitesi&rsquo;ni daha &ouml;nce ziyaret ettiğiniz anlaşılır ve size iletilecek i&ccedil;erik bu doğrultuda belirlenir ve b&ouml;ylelikle sizlere daha iyi bir hizmet sunulur.</p>
			</td>
		</tr>
	</tbody>
</table>





