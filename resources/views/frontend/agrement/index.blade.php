ise öncelikle o ödül puanlarından, mevcut değil ise SATICI'nın Tüketici'ye iade
edeceği bedelden nakden indirilerek (mahsup edilerek) yapılır.<br>
<br>
Tüketici tarafından işbu Sözleşme konusu Ürün alışverişinde SATICI'ya ödeme
kısmen/tamamen ödül puanları vb. ile yapılmış ise, bu suretle satın alınan Ürün
bedelinin Tüketici'ye geri ödenmesinin söz konusu olduğu hallerde, Tüketici'nin
Ürün'ü İNTERNET SİTESİ'nde satın alırken SATICI nezdindeki kullandığı ödül
puanı ve benzerleri, SATICI'nın ilgili kuruluş ile farklı bir anlaşması
olmadıkça, Tüketici'ye (yine puan olarak) iade edilebilir.<br>
<br>
Tüketici tarafından herhangi bir surette haksız ödül puanı kazanımı veya
kullanımının tespit edildiği durumlarda geçerli bir genel kural olarak, söz
konusu ödül puanlarının parasal değeri-tutarı SATICI tarafından Tüketici'den
(kredi kartından, nakden ve sair yasal yöntemler ile) tahsil olunabilecektir.
Bu hüküm, öyle bir sistemin uygulaması neticesi SATICI tarafından Tüketici'ye
hediye verilen malların bedeli için de geçerlidir.<br>
<br>
Ödül puanı ve benzerlerinin kazanım ve kullanım işlemleri ilgili diğer
hususlar, söz konusu kuruluş ile Tüketici ve SATICI arasındaki
anlaşma-sözleşmelerin hükümlerine tabi olup, ilgili hallerde SATICI, gerek
burada, gerek anılan sözleşme-anlaşmalarda belirli tüm hak-yetkileri Tüketici
ve kuruluş nezdinde kullanabilir, ilgili işlemleri belirtilen kuruluş ve/veya
aynı sistemdeki diğer işyerleri adına ve/veya hesabına da yapabilir.<br>
<br>
SATICI'dan kazanılan veya SATICI nezdinde kullanılan ödül puanları, hediye
çekleri vb. mukabili nakdi para talepleri, hiçbir durumda ve surette kabul
edilmez.<br>
<br>
SATICI, Tüketici ile yukarıda anılan kuruluşlar arasındaki ihtilaflar ile
bunların herhangi bir maddi, hukuki, mali ve mali olmayan sonucu için hiçbir
mesuliyet kabul etmemektedir; yukarıdaki hükümler geçerli ve saklıdır.&nbsp;<br>
<br>
Yukarıdaki hükümler, varsa, Tüketici'nin doğrudan SATICI'dan edindiği ödül
puanı kazanım ve kullanımlarında da kıyasen uygulanır.&nbsp;<br>
