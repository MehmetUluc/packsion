da kimliği belli olmayacak biçimde anonim hale getirilir. Tüketici isterse
kişisel verilerinin işlenmesi ile ilgili işlemler, aktarıldığı kişiler, eksik
veya yanlış olması halinde düzeltilmesi, düzeltilen bilgilerin ilgili üçüncü
kişilere bildirilmesi, verilerin silinmesi veya yok edilmesi, otomatik
sistemler ile analiz edilmesi sureti ile kendisi aleyhine bir sonucun ortaya
çıkmasına itiraz, verilerin kanuna aykırı olarak işlenmesi sebebi ile zarara uğrama
halinde giderilmesi gibi konularda SATICI'ya her zaman başvurabilir ve bilgi
alabilir. Bu hususlardaki başvuru ve talepleri yasal azami süreler içinde
yerine getirilecek yahut hukuki gerekçesi tarafına açıklanarak kabul
edilmeyebilecektir.&nbsp;<br>
